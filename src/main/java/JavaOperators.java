public class JavaOperators {
    /**
     * Operators are symbols that performs operations on variable and values
     * Types:->
     * Arithmatic      * Assignment      * Relational
     * logical      * Increment /decrement      * Bitwise
     */

    public static void main(String[] args) {
        int i= 10;
        int j= 21;
        int result;
        result= i+j; //Addition
        System.out.println(result);

        result = j-i; //Subtraction
        System.out.println(result);
    }
}
